.MODEL SMALL
.STACK 400h

.DATA
	SUCCESS_CHNG DB "Program: success modifying!!!$"

.CODE
START:
	MOV AX, @DATA
	MOV DS, AX
	XOR DX, DX	
MOD_ATTEMPT:
	MOV AX, 0
	TEST AX, AX
	JNZ MODIFY_DONE
	TEST DX, DX
	JNZ FINISH
	MOV BYTE PTR MOD_ATTEMPT, 40h
	MOV WORD PTR MOD_ATTEMPT + 1, 9090h
	INC DX
	JMP MOD_ATTEMPT
MODIFY_DONE:
	MOV	AH, 09h
	LEA DX, SUCCESS_CHNG
	INT 21h
FINISH:		
	MOV AX, 4C00h
	INT 21h	
END START
